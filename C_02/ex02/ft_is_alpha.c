int	ft_str_is_alpha(char *str)
{
	int i;
	int b;
	int c;

	i = 0;
	c = 1;
	while (str[i] != '\0')
	{
		b = str[i];
		if ((b < 65 || (b > 90 && b < 97) || b > 122))
			c = 0;
		i++;
	}
	return (c);
}

