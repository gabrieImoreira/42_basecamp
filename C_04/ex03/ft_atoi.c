int	ft_atoi(char *str)
{
	int i;
	int j;
	int n;

	i = 0;
	j = 1;
	n = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
		str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		n = (str[i] - '0') + (n * 10);
		i++;
	}
	n *= j;
	return (n);
}
